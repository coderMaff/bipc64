// Draw once per screen refresh
waitforraster:
	lda $d012								// Grab raster position
	cmp #208								// Check if its finished drawing play area
	bne waitforraster				// It hasnt

	jsr handlekeys
	jsr movesprites
	jsr drawhud							// Drawing the hud kills keyboard control
	jmp mainloop

end:
